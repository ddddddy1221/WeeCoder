async function confirmCurrentStageViaApi(app, projectId, actor = 'Test') {
  const response = await request(app).get(`/api/projects/${projectId}`).expect(200);
  const project = response.body.project;
  const entry = project.stageConfirmations?.[project.currentStageId];
  for (const item of entry?.items || []) {
    await request(app)
      .post(`/api/projects/${projectId}/stage-confirmations`)
      .send({
        actor,
        stageId: project.currentStageId,
        itemId: item.id,
        value: `Confirmed ${item.title}`,
      })
      .expect(200);
  }
}

async function advanceProjectViaApi(app, projectId, body = {}) {
  await confirmCurrentStageViaApi(app, projectId, body.actor || 'Test');
  return request(app).post(`/api/projects/${projectId}/advance`).send(body).expect(200);
}

function advanceConfirmed(project, options = {}) {
  return advanceProject(confirmStage(project, project.currentStageId, options.actor || 'Test'), options);
}

function confirmStage(project, stageId = project.currentStageId, actor = 'Test') {
  const normalized = normalizeProject(project);
  const entry = normalized.stageConfirmations?.[stageId];
  return (entry?.items || []).reduce(
    (nextProject, item) =>
      updateStageConfirmationForProject(nextProject, {
        actor,
        stageId,
        itemId: item.id,
        value: `Confirmed ${item.title}`,
      }),
    normalized,
  );
}

